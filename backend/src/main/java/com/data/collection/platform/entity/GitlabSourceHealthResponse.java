package com.data.collection.platform.entity;

import java.time.LocalDateTime;
import java.util.List;

public record GitlabSourceHealthResponse(
    Long configId,
    String name,
    String sourceInstance,
    boolean enabled,
    String healthStatus,
    String healthMessage,
    SyncStatus currentStatus,
    String currentMessage,
    LocalDateTime currentStartedAt,
    SyncStatus latestLogStatus,
    String latestLogMessage,
    LocalDateTime latestLogFinishedAt,
    int registeredMirrorTables,
    int existingMirrorTables,
    boolean factLayerLagging,
    String factLayerMessage,
    LocalDateTime latestFactUpdatedAt,
    boolean mergeRequestFactLagging,
    boolean issueFactLagging,
    boolean integrationTestFactLagging,
    long mergeRequestFactCount,
    long issueFactCount,
    long integrationTestFactCount,
    List<String> missingRequiredMirrorTables) {

  public GitlabSourceHealthResponse(
      Long configId,
      String name,
      String sourceInstance,
      boolean enabled,
      SyncStatus currentStatus,
      String currentMessage,
      LocalDateTime currentStartedAt,
      SyncStatus latestLogStatus,
      String latestLogMessage,
      LocalDateTime latestLogFinishedAt,
      int registeredMirrorTables,
      int existingMirrorTables,
      boolean factLayerLagging,
      String factLayerMessage,
      LocalDateTime latestFactUpdatedAt,
      boolean mergeRequestFactLagging,
      boolean issueFactLagging,
      boolean integrationTestFactLagging,
      long mergeRequestFactCount,
      long issueFactCount,
      long integrationTestFactCount,
      List<String> missingRequiredMirrorTables) {
    this(
        configId,
        name,
        sourceInstance,
        enabled,
        enabled ? "OK" : "DISABLED",
        "",
        currentStatus,
        currentMessage,
        currentStartedAt,
        latestLogStatus,
        latestLogMessage,
        latestLogFinishedAt,
        registeredMirrorTables,
        existingMirrorTables,
        factLayerLagging,
        factLayerMessage,
        latestFactUpdatedAt,
        mergeRequestFactLagging,
        issueFactLagging,
        integrationTestFactLagging,
        mergeRequestFactCount,
        issueFactCount,
        integrationTestFactCount,
        missingRequiredMirrorTables);
  }
}
