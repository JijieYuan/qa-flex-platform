    owner_name,
    reviewer_names,
    assignee_names,
    module_name,
    label_names,
    created_at_source,
    updated_at_source,
    ods_updated_at,
    merged_at_source,
    review_status,
    review_duration_minutes,
    comment_rate,
    comment_rate_source,
    defect_count,
    defect_count_source,
    scan_status,
    scan_bug_count,
    added_lines,
    deleted,
    fact_refreshed_at,
